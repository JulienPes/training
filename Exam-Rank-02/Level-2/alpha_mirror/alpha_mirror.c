#include <unistd.h>
#include <stdio.h>

int   main(int argc, char  **argv)
{
   int   i;

   i = 0;
   if (argc == 2)
   {
      while(argv[1][i])
      {
         char  c;
         c = argv[1][i];
         if (c >= 'a' && c <= 'z')
            c = 97 + 122 - c;
         else if (c >= 'A' && c <= 'Z')
            c = 65 + 90 - c;
         write(1 , &c, 1);
         i ++;
      }
   }
   write(1 , "\n", 1);
}